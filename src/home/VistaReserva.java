    public void cargarCabeceraReservas() {
        ArrayList<Object> co = new ArrayList<Object>();
        co.add("id_reserva");
        co.add("Nombre cliente");
        co.add("Fecha y hora");

        for (Object it : co) {
            modelo.addColumn(it);
        }

        jMesas.setModel(modelo);
    }

